var x = 7;
var y = "7";
var z = 2;

// equality
x == y // x "equals" y; true
x != y // x "not equal to" y; false

// strict equality 
x === y // x has the same data type and value as y; false
x !== y // x has either a different value OR data type as y; true


x < y  // false
x < z  // false
x <= y // true
x <= z // false
x > y  // false
x >= y // true
